/*Faça um programa que receba quatro números inteiros, calcule e mostre a soma desses números*/

#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a = 5;
    int b = 8;
    int c = 3;
    int d = 2;
    int soma = a + b + c + d;

    printf("A soma dos números %d, %d, %d e %d é: %d", a, b, c, d, soma);
}